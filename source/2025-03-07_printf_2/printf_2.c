#include <stdio.h>

int main(void)
{
	printf("10 + 3 = %d\n", 10 + 3);
	printf("10 - 3 = %d\n", 10 - 3);
	printf("10 x 3 = %d\n", 10 * 3);
	printf("10 ÷ 3 = %.1f\n", 10. / 3.);
	printf("10 ÷ 3 = (나머지) %d\n", 10 % 3);

	return 0;
}

